float rType(int r, float t) {
	switch (r) {
	case 0:
		t = 0.;
		break;
	case 2:
		t = t * t * (3. - 2. * t);
		break;
	case 3:
		t = pow(t, 2.);
		break;
    default:
        return t;
	}
	return t;
}
float setVal(vec3 arr[], float rrow, int size) {
	int R_INDX = 0;
	float t = 0;
	float renVal = 0;
	vec3 RET = { 0,0,0 };
	vec3 NEXT = { 0,0,0 };
	for (int i = 0; i < size; i++) {
		if (rrow <= arr[i].row) {
			R_INDX = i - 1;
			break;
		}
	}
	RET = arr[R_INDX];
	NEXT = arr[R_INDX + 1];
	t = (rrow - RET.row) / (NEXT.row - RET.row);
	renVal = RET.value + (NEXT.value - RET.value) * rType(RET.type, t);
	return renVal;
}
