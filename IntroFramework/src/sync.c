float setVal(vec3 arr[], float rrow, long size, long *R_INDX) {
    if(*R_INDX>0) {
        *R_INDX--;
    }
	float t = 0;
	float renVal = 0;
	vec3 RET = { 0,0,0 };
	vec3 NEXT = { 0,0,0 };
	for (long i = *R_INDX; i < size - 1; i++) {
		if (rrow <= arr[i].row) {
            if(i>0) {
			    *R_INDX = i - 1;
            }
			break;
		}
	}
    long index = *R_INDX;
	RET = arr[index];
	NEXT = arr[index + 1];
	t = (rrow - RET.row) / (NEXT.row - RET.row);
    t = rType(RET.type, t);
	renVal = RET.value + (NEXT.value - RET.value) * t;
	return renVal;
}
